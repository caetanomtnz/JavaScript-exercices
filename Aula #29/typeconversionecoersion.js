//        Type conversion (typecasting) vs Type coersion

//        * Alteração de um tipo de dado para outro tipo

//         As duas servem para alternar um tipo de dado para outro tipo


// resultado: 95
console.log ('9'+ 5) // Transformou um tipo number para um tipo string (isso foi o coersion(coersão)).


// resultado: 14
console.log (Number('9')+ 5) // Transformou para number (isso foi o type conversion).